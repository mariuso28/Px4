package org.dx4.bet;

public class UserAmountMap 
{
	private long userId;
	private double amount;
	
	public UserAmountMap()
	{
	}
	
	public long getUserId() {
		return userId;
	}
	public void setUserId(long userId) {
		this.userId = userId;
	}
	public double getAmount() {
		return amount;
	}
	public void setAmount(double amount) {
		this.amount = amount;
	}
	
	
}
