package org.dx4.game.persistence;

public class ImageHolder {

	private String name;
	private String image;
	
	public ImageHolder()
	{
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}
	
	
}
