package org.dx4.admin;

public class Dx4Version {

	private String code;
	private byte[] apk;
	
	public Dx4Version()
	{
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public byte[] getApk() {
		return apk;
	}

	public void setApk(byte[] apk) {
		this.apk = apk;
	}
	
}
